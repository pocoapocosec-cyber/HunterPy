"""AI-consumable Markdown reconnaissance report.

The output of this writer is meant to be copy/pasted directly into an AI
assistant (ChatGPT / Claude / Gemini) for next-step guidance. Therefore:
  * sections appear in a stable order
  * data is presented as tables / tight bullet lists (no raw JSON dumps)
  * no actual secret values are leaked — only the fact that something was
    flagged
  * a clear "How to use this report" block sits near the top so a human
    sees it before sending it to an AI
"""
from __future__ import annotations

from datetime import datetime
from typing import Any, Dict, Iterable, List, Optional


TOOL_VERSION = "2.0.0"
TOOL_NAME    = "HunterPy"


# --------------------------------------------------------------------------
def render_markdown(target: str,
                    metadata: Dict[str, Any],
                    findings: List[Dict[str, Any]],
                    recon: Optional[Dict[str, Any]] = None,
                    surface: Optional[Dict[str, Any]] = None,
                    javascript: Optional[Dict[str, Any]] = None,
                    dns_records: Optional[Dict[str, Any]] = None,
                    whois_data: Optional[Dict[str, Any]] = None,
                    cookies: Optional[List[Dict[str, Any]]] = None,
                    dorks: Optional[Dict[str, Any]] = None) -> str:
    """Return a Markdown string. All callers should use this single function."""
    recon      = recon or {}
    surface    = surface or {}
    javascript = javascript or {}
    dns_records = dns_records or {}
    whois_data = whois_data or {}
    cookies = cookies or []

    summary = build_findings_summary(findings, surface, javascript, cookies)

    parts: List[str] = []
    # YAML frontmatter — machine-readable summary for LLM ingestion + CI
    # pipelines. Humans skip it; LLMs love it. Schema is documented in
    # the README "Reports" section.
    parts.append(_yaml_frontmatter(target, metadata, findings))
    parts.append(_h1(f"Reconnaissance Report — {target}"))
    parts.append(f"_Generated by {TOOL_NAME} v{TOOL_VERSION} on "
                 f"{metadata.get('end_time') or datetime.utcnow().isoformat() + 'Z'}_\n")

    # --- legal + instructions front-and-center ---------------------------
    parts.append(_h2("Legal Notice"))
    parts.append("> This report was produced by an authorized security "
                 "assessment. Do not use it against systems you do not own "
                 "or do not have explicit written permission to test.\n")

    parts.append(_h2("How To Use This Report"))
    parts.append(
        "Copy the **entire contents of this Markdown file** and paste it into "
        "an AI assistant such as ChatGPT, Claude, or Gemini. Ask it to:\n"
        "\n"
        "1. Analyze the findings below and rank them by exploitability.\n"
        "2. Suggest specific vulnerability classes worth investigating based "
        "on the detected technology stack and exposed surface.\n"
        "3. Recommend the next manual tests to perform (no automated attacks).\n"
        "4. Highlight any findings that suggest a chained / multi-step attack.\n"
        "\n"
        "This tool performs **passive reconnaissance only** — no attack "
        "payloads were sent. The AI's role is strategic guidance, not "
        "exploitation.\n"
    )

    # --- scan metadata ---------------------------------------------------
    parts.append(_h2("Scan Metadata"))
    parts.append(_kv_table([
        ("Target",      target),
        ("Mode",        metadata.get("mode", "n/a")),
        ("Scan ID",     metadata.get("scan_id", "n/a")),
        ("Start time",  metadata.get("start_time", "n/a")),
        ("End time",    metadata.get("end_time", "n/a")),
        ("Duration",    metadata.get("duration", "n/a")),
        ("Modules run", ", ".join(metadata.get("modules_run", []) or [])),
    ]))

    # --- tech ------------------------------------------------------------
    parts.append(_h2("Technology Stack"))
    parts.append(_render_tech(findings, recon))

    # --- headers ---------------------------------------------------------
    parts.append(_h2("Security Header Audit"))
    parts.append(_render_header_audit(findings, recon))

    # --- cookies ---------------------------------------------------------
    parts.append(_h2("Cookie Security"))
    parts.append(_render_cookies(cookies))

    # --- DNS -------------------------------------------------------------
    parts.append(_h2("DNS Records"))
    parts.append(_render_dns(dns_records))

    # --- WHOIS -----------------------------------------------------------
    parts.append(_h2("WHOIS Information"))
    parts.append(_render_whois(whois_data))

    # --- discovered links ------------------------------------------------
    parts.append(_h2("Discovered Links"))
    parts.append(_render_links(surface))

    # --- forms -----------------------------------------------------------
    parts.append(_h2("Forms Discovered"))
    parts.append(_render_forms(surface.get("forms", [])))

    # --- URL params ------------------------------------------------------
    parts.append(_h2("URL Parameters Found"))
    parts.append(_render_params(surface.get("url_params", [])))

    # --- sensitive paths -------------------------------------------------
    parts.append(_h2("Sensitive Path Check"))
    parts.append(_render_path_check(surface.get("sensitive_paths", [])))

    # --- JS analysis -----------------------------------------------------
    parts.append(_h2("JavaScript Analysis"))
    parts.append(_render_javascript(javascript))

    # --- Google dorks ----------------------------------------------------
    parts.append(_h2("Google Dorks (for manual review)"))
    parts.append(_render_dorks(dorks or {}))

    # --- verification results -------------------------------------------
    verified = [f for f in findings if f.get("verification")]
    if verified:
        parts.append(_h2("Verification Results (Phase 6 — safe probes)"))
        parts.append(_render_verifications(verified))

    # --- summary + next steps -------------------------------------------
    parts.append(_h2("Summary of Notable Findings"))
    if summary:
        for s in summary:
            parts.append(f"- {s}")
        parts.append("")
    else:
        parts.append("_No notable findings flagged._\n")

    parts.append(_h2("Next Steps"))
    parts.append(
        "This report was designed to be pasted into an AI assistant for "
        "further analysis. After pasting, prompt the AI for: (a) the most "
        "promising vulnerability classes to investigate given the technology "
        "stack and exposed surface, (b) specific manual tests to run against "
        "the discovered endpoints and forms, (c) which areas of the "
        "application are most likely to contain security weaknesses, and (d) "
        "any chained / multi-step attacks suggested by the combination of "
        "findings. **No automated exploitation should be performed — use the "
        "AI's suggestions to guide manual, authorized testing only.**\n"
    )
    return "\n".join(parts)


