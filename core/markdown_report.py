def build_findings_summary(findings: List[Dict[str, Any]],
                           surface: Dict[str, Any],
                           javascript: Dict[str, Any],
                           cookies: List[Dict[str, Any]]) -> List[str]:
    """Short list of plain-English bullets — the part an AI grabs first."""
    out: List[str] = []

    interesting = [f for f in findings if f.get("classification") == "INTERESTING"]
    if interesting:
        out.append(f"{len(interesting)} INTERESTING finding(s) — investigate first.")

    missing = [f["title"] for f in findings
               if f.get("type") == "missing_security_header"]
    if missing:
        out.append("Missing security headers: "
                   + ", ".join(sorted({m.split(': ', 1)[-1] for m in missing})) + ".")

    weak_csp = [f for f in findings if f.get("type") == "weak_csp"]
    if weak_csp:
        out.append(f"Content-Security-Policy contains weak directives "
                   f"({len(weak_csp)} issue(s)).")

    ssl_issues = [f for f in findings if f.get("module") == "ssl"
                  and f.get("classification") in ("INTERESTING", "COMMON")]
    if ssl_issues:
        out.append(f"{len(ssl_issues)} TLS/cert finding(s) reported.")

    path_hits = [p for p in surface.get("sensitive_paths", [])
                 if p.get("status_code") and 200 <= p["status_code"] < 300
                 and p.get("has_content")]
    if path_hits:
        out.append("Sensitive paths reachable with non-empty body: "
                   + ", ".join(p["url"].rsplit("/", 1)[-1] or p["url"]
                               for p in path_hits) + ".")

    if javascript.get("sensitive_keyword_hits"):
        out.append(f"{len(javascript['sensitive_keyword_hits'])} sensitive "
                   "keyword occurrence(s) flagged in JavaScript files "
                   "(values not logged).")

    if javascript.get("endpoints"):
        out.append(f"{len(javascript['endpoints'])} API/GraphQL endpoint(s) "
                   "referenced in JavaScript source.")

    if surface.get("forms"):
        login_forms = [f for f in surface["forms"]
                       if any(i.get("type") == "password" for i in f.get("fields", []))]
        if login_forms:
            out.append(f"{len(login_forms)} login form(s) discovered.")

    cookie_issues = []
    for c in cookies:
        flags = []
        if not c.get("httponly"): flags.append("HttpOnly")
        if not c.get("secure"):   flags.append("Secure")
        if not c.get("samesite"): flags.append("SameSite")
        if flags:
            cookie_issues.append(f"{c.get('name', '?')} missing {'+'.join(flags)}")
    if cookie_issues:
        out.append("Cookies missing security flags: "
                   + "; ".join(cookie_issues[:6]) + ".")

    cves = [f for f in findings if f.get("type") == "cve"]
    if cves:
        top = sorted(cves, key=lambda x: -float(x.get("cvss") or 0))[:3]
        out.append("Possible CVEs based on disclosed software version: "
                   + ", ".join(
                       f"{c['evidence'].get('cve_id', '?')} (CVSS "
                       f"{c.get('cvss', '?')})" for c in top) + ".")
    return out


